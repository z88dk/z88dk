isr_t ISR(void)
{
    M_PRESERVE_MAIN;

    unsigned char vline = IO_VLINE; // Read the VLINE value
    unsigned int VCTRL = IO_VCTRL;

    if (VCTRL & VCRTL_80COL_EN)
    {
        // 80x25 mode so enable 2nd text page
        // to access to color RAM
        IO_VCTRL = VCTRL | VCTRL_TEXT_PAGE;
    }

    if (vline < 50)
    {
        BORDER(1);
        IO_VIRQLINE = 50;
    }
    else if (vline < 100)
    {
        BORDER(2);
        IO_VIRQLINE = 100;
    }
    else if (vline < 150)
    {
        BORDER(3);
        IO_VIRQLINE = 150;
    }
    else if (vline < 200)
    {
        BORDER(4);
        IO_VIRQLINE = 200;
    }
    else if (vline < 250)
    {
        BORDER(5);
        IO_VIRQLINE = 250;
    }
    else
    {
        BORDER(0);
        IO_VIRQLINE = 0;
    }

    // Restore original value
    IO_VCTRL = VCTRL;

    // Clear the VLINE interrupt
    IO_IRQSTAT = IRQ_VLINE;

    M_RESTORE_MAIN;
}

void displayInfo(void)
{
    unsigned int val;
    unsigned int mode = IO_VCTRL;

    console_ioctl(IOCTL_GENCON_CONSOLE_SIZE, &val);

    printf("%cInterrupt example in %dx%d %s mode\n", 12, val & 0xff, val >> 8, (mode & 1) ? "text" : "bitmap");

    for (char a = 1; a < 24; a++)
        printf("Row %d\n", a);
    printf("Press ENTER to scroll");

    // Wait for a key to be pressed
    getch();

    printf("\nPress ENTER again.");

    getch();
}

void main(void)
{
    int val;

    // Load a custom font by damieng
    // https://damieng.com/typography/zx-origins/outrunner/
    val = (unsigned int) Outrunner_Outline_ch8;
    console_ioctl(IOCTL_GENCON_SET_FONT32, &val);

    // Setup im1 interrupts
    im1_init();

    // Install user ISR
    add_raster_int(ISR);

    // Enable the VLINE interrupt at line 0
    IO_VIRQLINE = 0;
    IO_IRQMASK = IRQ_VLINE;

    displayInfo();

    // Set 80 column mode
    val = VCTRL_TEXT_EN | VCTRL_REMAP_BC | VCRTL_80COL_EN;
    console_ioctl(IOCTL_GENCON_SET_MODE, &val);

    displayInfo();

    // Set hires bitmap mode
    val = VCTRL_MODE_BM | VCTRL_REMAP_BC;
    console_ioctl(IOCTL_GENCON_SET_MODE, &val);

    displayInfo();

    // Back to 40x25 mode
    val = VCTRL_TEXT_EN | VCTRL_REMAP_BC;
    console_ioctl(IOCTL_GENCON_SET_MODE, &val);

    // Set border back to black
    intrinsic_di();
    bordercolor(0);
}
