// To change the border color in text mode
// just write to this address.
// in 80-column mode, you need to page in
// the 2nd text page.
#define BORDER(a) *((unsigned char *)BORDERCLR) = (a)

// To change the border color in bitmap mode you
// need to change palette index 0
#define BMBORDER(a)                  \
    {                                \
        IO_VPALSEL = 0;              \
        IO_VPALDATA = rgb[a] & 0xff; \
        IO_VPALSEL = 1;              \
        IO_VPALDATA = rgb[a] >> 8;   \
    }

