ansi_CHAR:
    ld      hl, char+1
; --- TO USE ROM FONT WE NEED TO MAP TO THE ASCII CODES ---
     IF    ROMFONT
       ld      e, a
   
       xor     a
       ld      (UCASE), a
       ld      (UCASE+1), a
       ld      (UCASE+2), a
       ld      a, (BOLD)
       xor     $17                         ; rla
       ld      (UCASE+3), a
   
   
   ;	ld	a,ansifont/256
   ;	cp $1F	; ROM font ?
       ld      a, e
   ;	jr	nz,norom
       ld      (hl), a
       call    asctozx81
   
       ld      e, a
       rla
       ld      a, e
       jr      nc, noupr
       ld      a, $23                      ; inc hl
       ld      (UCASE), a
       ld      a, $b6                      ; or (hl)
       ld      (UCASE+1), a
       ld      a, $2b                      ; dec hl
       ld      (UCASE+2), a
   ;	xor a
   ;	ld	(UCASE+3),a
       ld      a, e
       and     $7f
   noupr:
     ENDIF
   ; --- END OF ROM FONT ADAPTER ---
norom:
    ld      (hl), a
    ld      a, (__console_y)            ; Line text position

; WRX, 384 rows, 1st page (font height = 12/2)
    ld      h, a
	ld      l, 0
	rr      h
	rr      l                          ; A*128
	ld      d, h
	ld      e, l
	rr      h
	rr      l                          ; A*64
	add     hl,de                      ; --> A*192
    ld      de, (base_graphics)
    add     hl, de

    ld      (RIGA+1), hl
;  xor a
    ld      hl, DOTS+1
    ld      b, (hl)
    ld      hl, 0

    ld      a, (__console_x)            ; Column text position
    ld      e, a
    ld      d, 0
    or      d
    jr      z, ZCL
LP:
    add     hl, de
    djnz    LP

    srl     h
    rr      l
    rra
    srl     h
    rr      l
    rra
    srl     h
    rr      l
    rra

    srl     a
    srl     a
    srl     a
    srl     a
    srl     a
;  ld b,5
;.RGTA
;  srl a
;  djnz RGTA
ZCL:
    ld      (PRE+1), a
    ld      e, a
    ld      a, (DOTS+1)
    add     a, e
    ld      e, a
    ld      a, 16
    sub     e
NOC:
    ld      (POST+1), a
RIGA:                                   ; Location on screen
    ld      de, 16384
    add     hl, de
    push    hl
    pop     ix
char:
    ld      b, 'A'                      ; Put here the character to be printed

  ;ld hl,ansifont-256
  IF    ROMFONT
    ld      hl, $1E00
    xor     a
    add     b
    jr      z, NOLFONT
  ELSE

    ;ld      a, ansifont_is_packed
    ;ld      hl, ansifont-(32*12)
IF FONT11
	ld      hl, wrxi_font-(' '*11)
ELSE
	ld      hl, wrxi_font-(' '*12)
ENDIF

    ;and     a
;    jr      z, got_font_location
;
;    xor     a
;    rr      b
;    jr      c, even
;    ld      a, 4
;even:
;    ld      (ROLL+1), a
;    ld      hl, ansifont-128
;got_font_location:

  ENDIF

  IF    ROMFONT
    ld      de, 8                         ; 6x12 (4 blank rows added automatically)
  ELSE
    IF FONT11
      ld      de, 11                      ; 6x12 (1 blank row added automatically)
    ELSE
      ld      de, 12
    ENDIF
  ENDIF
LFONT:
    add     hl, de
    djnz    LFONT
NOLFONT:

;  IF    !ARX816
;    IF  G007
;    ld      de, 34                      ; next row
;    ELSE
;      IF    MTHRG
;    ld      de, 33                      ; next row
;      ELSE
;    ld      de, 32                      ; next row
;      ENDIF
;    ENDIF
;  ENDIF

  IF    ROMFONT
    ld      c, 8
  ELSE
    ld      c, 12
  ENDIF

PRE:
    ld      b, 4
  IF    ARX816
    rl      (ix+8)
  ELSE
    rl      (ix+1)
  ENDIF
    rl      (ix+0)
    inc     b
    dec     b
    jr      z, DTS
L1:
  IF    ARX816
    rl      (ix+8)
  ELSE
    rl      (ix+1)
  ENDIF
    rl      (ix+0)
    djnz    L1
DTS:


    ld      a, (hl)

   IF    ROMFONT
UCASE:
    nop
    nop
    nop
    rla                                 ; shift character left to squeeze it as much as possible (36..42 columns still usable)
   ENDIF

BOLD:
    nop                                 ;	rla
    nop                                 ;	or (hl)
    ld      b, a
;    ld      a, ansifont_is_packed
;    and     a
;    ld      a, b
;    jr      z, INVRS
;
;ROLL:
;    jr      INVRS
;    rla
;    rla
;    rla
;    rla
;
IF    ROMFONT
    bit     3,c
    jr      z,no_wipe
	xor     a    ; force to blank the bottom of the character
no_wipe:
ELSE
  IF FONT11
; --- trick to save little more than 80 bytes keeping a 6x11 font for a 6x12 matrix --
    dec     c
    jr   nz,no_last
	xor     a    ; force to blank the last character row
no_last:
    inc     c
; --- --------------------------- --
  ENDIF
ENDIF

INVRS:
;  cpl           ; Set to NOP to disable INVERSE
    nop

; Underlined text handling
    dec     c
;  jr nz,UNDRL   ; Set to JR UNDRL to disable underlined text (loc. INVRS+2)
    jr      UNDRL
    ld      a, 255                      ; ..a possible variant is CPL
UNDRL:
    inc     c
; end of underlined text handling

DOTS:
    ld      b, ansicharacter_pixelwidth
;IF ROMFONT
;    ld      b, 7  ; 36 columns
;ELSE
;    ld      b, 6  ; 42 columns
;ENDIF
L2:
    rla
    rl      (ix+1)
    rl      (ix+0)
    djnz    L2
POST:
    ld      b, 6
    inc     b
    dec     b
    jr      z, NEXT
L3:
    rl      (ix+1)
    rl      (ix+0)
    djnz    L3
NEXT:

	ld      de,6144
	bit     0,c
	jr      z,even_row
    ld      de,-6144+32
even_row:
    add     ix, de

    inc     hl
    dec     c
    jr      nz, PRE
    ret


